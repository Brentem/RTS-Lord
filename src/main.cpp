extern "C"
{
    #include "raylib.h"
}

#include "../include/Game.h"

int main(void) 
{
    // Initialization
    //--------------------------------------------------------------------------------------
    Game game;
    // //--------------------------------------------------------------------------------------

    // Main game loop
    while (!WindowShouldClose())    // Detect window close button or ESC key
    {

        // Update
        //----------------------------------------------------------------------------------
        game.HandleInput();
        game.Update();
        //----------------------------------------------------------------------------------

        // Draw
        //----------------------------------------------------------------------------------
        game.Render();
        //----------------------------------------------------------------------------------
    }

    //--------------------------------------------------------------------------------------
    CloseWindow();        // Close window and OpenGL context
    //--------------------------------------------------------------------------------------

    return 0;
}